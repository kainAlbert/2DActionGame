package Application;

public class GSvector2 {

	public double x;
	public double y;

	// コンストラクタ
	public GSvector2(){

		x = 0;
		y = 0;
	}

	// コンストラクタ
	public GSvector2( double x, double y ){

		this.x = x;
		this.y = y;
	}

	// コンストラクタ
	public GSvector2( int x, int y ){

		this.x = x;
		this.y = y;
	}
}
